#include <stdio.h>

#define TRUE 1
#define FALSE 0

int main() {
	int p,q,r,t,c;
	p=q=r=t=TRUE;
	c=FALSE;

	printf("===================================================================================\n");
	printf("Author: Jiwon Yoo\n\n");
	printf("===================================================================================\n");
	printf("  Commutative Laws: p&&q=q&&p		p||q==q||p\n");
	printf("-----------------------------------------------------------------------------------\n");
	printf("%3c%3c%6s%6s%6s%6s\n",'p','q',"p&&q","q&&p","p||q","q||p");
	printf("%3c%3d%5d%5d%6d%6d\n",p,q,p&&q,q&&p,p||q,q||p);
	printf("%3d%3d%5d%5d%6d%6d\n",p,!q,p&&!q,!q&&p,p||!q,!q||p);
	printf("%3d%3d%5d%5d%6d%6d\n",!p,q,!p&&q,q&&!p,!p||q,q||!p);
	printf("%3d%3d%5d%5d%6d%6d\n",!p,!q,!p&&!q,!q&&!p,!p||!q,!q||!p);
	printf("===================================================================================\n");

	printf("  Associative Laws: (p&&q)&&r=p&&(q&&r)   (p||q)||r=p||(q||r)\n");
	printf("-----------------------------------------------------------------------------------\n");
	printf("%3c%3c%3c%6s%6s%6s%6s%12s%12s%12s%12s\n",'p','q','r',"p&&q","q&&p","p||q","q||r","(p&&q)&&r)","p&&(q&&r)","(p||q)||r","p||(q||r)");
	printf("%3d%3d%3d%5d%5d%6d%6d%10d%12d%12d%11d\n",p,q,r,p&&q,q&&r,p||q,q||r,(p&&q)&&r,p&&(q&&r),(p||q)||r,p||(q||r));
	printf("%3d%3d%3d%5d%5d%6d%6d%10d%12d%12d%11d\n",p,q,!r,p&&q,q&&!r,p||q,q||!r,(p&&q)&&!r,p&&(q&&!r),(p||q)||!r,p||(q||!r));
	printf("%3d%3d%3d%5d%5d%6d%6d%10d%12d%12d%11d\n",p,!q,r,p&&!q,!q&&r,p||!q,!q||r,(p&&!q)&&r,p&&(!q&&r),(p||!q)||r,p||(!q||r));
	printf("%3d%3d%3d%5d%5d%6d%6d%10d%12d%12d%11d\n",p,!q,!r,p&&!q,!q&&!r,p||!q,!q||!r,(p&&!q)&&!r,p&&(!q&&!r),(p||!q)||!r,p||(!q||!r));
	printf("%3d%3d%3d%5d%5d%6d%6d%10d%12d%12d%11d\n",!p,q,r,!p&&q,q&&r,!p||q,q||r,(!p&&q)&&r,!p&&(q&&r),(!p||q)||r,!p||(q||r));
	printf("%3d%3d%3d%5d%5d%6d%6d%10d%12d%12d%11d\n",!p,q,!r,!p&&q,q&&!r,!p||q,q||!r,(!p&&q)&&!r,!p&&(q&&!r),(!p||q)||!r,!p||(q||!r));
	printf("%3d%3d%3d%5d%5d%6d%6d%10d%12d%12d%11d\n",!p,!q,r,!p&&!q,!q&&r,!p||!q,!q||r,(!p&&!q)&&r,!p&&(!q&&r),(!p||!q)||r,!p||(!q||r));
	printf("%3d%3d%3d%5d%5d%6d%6d%10d%12d%12d%11d\n",!p,!q,!r,!p&&!q,!q&&!r,!p||!q,!q||!r,(!p&&!q)&&!r,!p&&(!q&&!r),(!p||!q)||!r,!p||(!q||!r));
	printf("===================================================================================\n");

	printf("  Distributive Laws: p&&(q||r)=(p&&q)||(p&&r)     p||(q&&r)=(p||q)&&(p||r)   \n");
	printf("-----------------------------------------------------------------------------------\n");
	printf("%3c%3c%3c%6s%6s%6s%6s%6s%6s%12s%16s%12s%16s\n",'p','q','r',"q||r","p&&q","p&&r","q&&r","p||q","p||r","p&&(q||r)","(p&&q)||(p&&r)","p||(q&&r)","(p||q)&&(p||r)");
	printf("%3d%3d%3d%4d%6d%6d%6d%6d%6d%9d%14d%15d%14d\n",p,q,r,q||r,p&&q,p&&r,q&&r,p||q,p||r,p&&(q||r),(p&&q)||(p&&r),p||(q&&r),(p||q)&&(p||r));
	printf("%3d%3d%3d%4d%6d%6d%6d%6d%6d%9d%14d%15d%14d\n",p,q,!r,q||!r,p&&q,p&&!r,q&&!r,p||q,p||!r,p&&(q||!r),(p&&q)||(p&&!r),p||(q&&!r),(p||q)&&(p||!r));
	printf("%3d%3d%3d%4d%6d%6d%6d%6d%6d%9d%14d%15d%14d\n",p,!q,r,!q||r,p&&!q,p&&r,!q&&r,p||!q,p||r,p&&(!q||r),(p&&!q)||(p&&r),p||(!q&&r),(p||!q)&&(p||r));
	printf("%3d%3d%3d%4d%6d%6d%6d%6d%6d%9d%14d%15d%14d\n",p,!q,!r,!q||!r,p&&!q,p&&!r,!q&&!r,p||!q,p||!r,p&&(!q||!r),(p&&!q)||(p&&!r),p||(!q&&!r),(p||!q)&&(p||!r));
	printf("%3d%3d%3d%4d%6d%6d%6d%6d%6d%9d%14d%15d%14d\n",!p,q,r,q||r,!p&&q,!p&&r,q&&r,!p||q,!p||r,!p&&(q||r),(!p&&q)||(!p&&r),!p||(q&&r),(!p||q)&&(!p||r));
	printf("%3d%3d%3d%4d%6d%6d%6d%6d%6d%9d%14d%15d%14d\n",!p,q,!r,q||!r,!p&&q,!p&&!r,q&&!r,!p||q,!p||!r,!p&&(q||!r),(!p&&q)||(!p&&!r),!p||(q&&!r),(!p||q)&&(!p||!r));
	printf("%3d%3d%3d%4d%6d%6d%6d%6d%6d%9d%14d%15d%14d\n",!p,!q,r,!q||r,!p&&!q,!p&&r,!q&&r,!p||!q,!p||r,!p&&(!q||r),(!p&&!q)||(!p&&r),!p||(!q&&r),(!p||!q)&&(!p||r));
	printf("%3d%3d%3d%4d%6d%6d%6d%6d%6d%9d%14d%15d%14d\n",!p,!q,!r,!q||!r,!p&&!q,!p&&!r,!q&&!r,!p||!q,!p||!r,!p&&(!q||!r),(!p&&!q)||(!p&&!r),!p||(!q&&!r),(!p||!q)&&(!p||!r));
	printf("===================================================================================\n");

	printf("  Identity Laws: p&&t=p       p||c=p\n");
	printf("-----------------------------------------------------------------------------------\n");
	printf("%3c%3c%3c%7s%7s\n",'p','t','c',"p&&t","p||c");
	printf("%3d%3d%3d%5d%7d\n",p,t,c,p&&t,p||c);
	printf("%3d%3d%3d%5d%7d\n",!p,t,c,!p&&t,!p||c);
	printf("===================================================================================\n");

	printf("  Negation Laws: p||~p=t       p&&~p=c\n");
	printf("-----------------------------------------------------------------------------------\n");
	printf("%3c%3c%3c%7s%7s\n",'p','t','c',"p||~p","p&&~p");
	printf("%3d%3d%3d%5d%7d\n",p,t,c,p||!p,p&&!p);
	printf("%3d%3d%3d%5d%7d\n",!p,t,c,!p||!!p,!p&&!!p);
	printf("===================================================================================\n");

	printf("  Double Negative Laws: ~(~p)=p\n");
	printf("-----------------------------------------------------------------------------------\n");
	printf("%3c%5s%7s\n",'p',"~p","~(~p)");
	printf("%3d%5d%6d\n",p,!p,!(!p));
	printf("%3d%5d%6d\n",!p,!!p,!(!!p));
	printf("===================================================================================\n");

	printf("  Idempotent Laws: p&&p=p       p||p=p\n");
	printf("-----------------------------------------------------------------------------------\n");
	printf("%3c%7s%7s\n",'p',"p&&p","p||p");
	printf("%3d%5d%7d\n",p,p&&p,p||p);
	printf("%3d%5d%7d\n",!p,!p&&!p,!p||!p);
	printf("===================================================================================\n");

	printf("  Universal Bound Laws: p||t=t       p&&c=c\n");
	printf("-----------------------------------------------------------------------------------\n");
	printf("%3c%3c%3c%7s%7s\n",'p','t','c',"p||t","p&&c");
	printf("%3d%3d%3d%5d%7d\n",p,t,c,p||t,p&&c);
	printf("%3d%3d%3d%5d%7d\n",!p,t,c,!p||t,!p&&c);
	printf("===================================================================================\n");

	printf("  De Morgan's Laws: !(p&&q)=!p||!q		!(p||q)=!p&&!q\n");
	printf("-----------------------------------------------------------------------------------\n");
	printf("%3c%3c%4s%4s%6s%10s%10s%10s%8s\n",'p','q',"!p","!q","p&&q","!(p&&q)", "!p||!q", "!(p||q)", "!p&&!q");
	printf("%3d%3d%4d%4d%4d%9d%11d%9d%9d\n",p,q,!p,!q,p&&q,!(p&&q),!p||!q,!(p||q),!p&&!q);
	printf("%3d%3d%4d%4d%4d%9d%11d%9d%9d\n",p,!q,!p,!!q,p&&!q,!(p&&!q),!p||!!q,!(p||!q),!p&&!!q);
	printf("%3d%3d%4d%4d%4d%9d%11d%9d%9d\n",!p,q,!!p,!q,!p&&q,!(!p&&q),!!p||!q,!(!p||q),!!p&&!q);
	printf("%3d%3d%4d%4d%4d%9d%11d%9d%9d\n",!p,!q,!!p,!!q,!p&&!q,!(!p&&!q),!!p||!!q,!(!p||!q),!!p&&!!q);
	printf("===================================================================================\n");

	printf("  Absorption Laws: p||(p&&q)=p		p&&(p||q)=p\n");
	printf("-----------------------------------------------------------------------------------\n");
	printf("%3c%3c%6s%6s%12s%12s\n",'p','q',"p&&q","p||q", "p||(p&&q)", "p&&(p||q)");
	printf("%3d%3d%4d%6d%10d%11d\n",p,q,p&&q,p||q,p||(p&&q), p&&(p||q));
	printf("%3d%3d%4d%6d%10d%11d\n",p,!q,p&&!q,p||!q,p||(p&&!q), p&&(p||!q));
	printf("%3d%3d%4d%6d%10d%11d\n",!p,q,!p&&q,!p||q,!p||(!p&&q), !p&&(!p||q));
	printf("%3d%3d%4d%6d%10d%11d\n",!p,!q,!p&&!q,!p||!q,!p||(!p&&!q), !p&&(!p||!q));
	printf("===================================================================================\n");

	printf("  Negations of t and c: ~t=c       ~c=t\n");
	printf("-----------------------------------------------------------------------------------\n");
	printf("%3c%3c%5s%5s\n",'t','c',"~t","~c");
	printf("%3d%3d%5d%5d\n",t,c,!t,!c);
	printf("%3d%3d%5d%5d\n",!t,!c,!!t,!!c);
	printf("===================================================================================\n");
	return 0;
}
